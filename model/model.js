export function getPageContent(pageId, callback) {
  $("#app").html(eval(pageId));
  if (callback) {
    callback();
  }
}

export function removeBlackNav() {
  $("nav").removeClass("blackNav");
  $("nav").addClass("whiteNav");
  $(".blackLogo").removeClass("blackLogo").addClass("whiteLogo");
  $(".blackLinks").removeClass("blackLinks").addClass("whiteLinks");
  $(".dropdown-black").removeClass("dropdown-black").addClass("dropdown");
}

export function removeWhiteNav() {
  $("nav").removeClass("whiteNav");
  $("nav").addClass("blackNav");
  $(".whiteLogo").removeClass("whiteLogo").addClass("blackLogo");
  $(".whiteLinks").removeClass("whiteLinks").addClass("blackLinks");
  $(".dropdown").removeClass("dropdown").addClass("dropdown-black");
}

export function addGalleryListeners() {
  $(".gallery .gallery-content a").click(function (event) {
    let btnId = this.id;
    let contentId = btnId + "Content";
    console.log(contentId);
    getPageContent(contentId, addGalleryListeners);
  });
}

export function addBlogListeners() {
  $(".blog .blog-row .blog-holder").click(function (event) {
    let btnId = this.id;
    let contentId = btnId + "Content";
    console.log(contentId);
    getPageContent(contentId, addBlogListeners);
  });
}
